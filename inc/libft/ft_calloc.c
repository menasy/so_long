#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	size_t	len;
	void	*at;

	len = count * size;
	at = malloc(len);
	if (at == NULL)
		return (NULL);
	ft_bzero(at, len);
	return (at);
}
